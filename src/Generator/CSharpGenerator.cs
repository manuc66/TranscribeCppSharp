using System.Text;
using System.Text.RegularExpressions;

namespace TranscribeCppSharp.Generator;

/// <summary>
/// Generates C# P/Invoke declarations from parsed Rust FFI.
/// </summary>
public class CSharpGenerator
{
    private readonly string _dllName = "transcribe";

    private static readonly Dictionary<string, string> s_opaqueHandles = new()
    {
        ["transcribe_model"] = "ModelHandle",
        ["transcribe_session"] = "SessionHandle",
    };

    private static readonly (string Pattern, string Category)[] s_functionCategories =
    [
        ("model_load_params", "Model"), ("model_free", "Model"), ("model_", "Model"),
        ("session_init", "Session"), ("session_free", "Session"),
        ("session_limits", "Session"), ("session_", "Session"),
        ("stream_", "Streaming"), ("batch_", "Batch"), ("run_batch", "Batch"),
        ("run_params", "Run"), ("run", "Run"),
        ("segment_", "Results — Segments"), ("word_", "Results — Words"),
        ("token", "Results — Tokens"), ("speaker_", "Results — Speaker"),
        ("timing", "Timings"), ("backend", "Backend"), ("device", "Backend"),
        ("log_", "Logging"), ("version", "Version"), ("abi_", "ABI Metadata"),
        ("open", "Lifecycle"), ("close", "Lifecycle"), ("get_model", "Lifecycle"),
        ("init_backends", "Backend"), ("capabilities", "Model"), ("supports", "Model"),
        ("arch_string", "Model"), ("variant_string", "Model"), ("meta_val", "Model"),
        ("set_abort", "Cancellation"), ("was_aborted", "Cancellation"),
        ("was_truncated", "Cancellation"), ("n_segments", "Results"),
        ("n_words", "Results"), ("n_tokens", "Results"), ("full_text", "Results"),
        ("raw_text", "Results"), ("detected_language", "Results"),
        ("returned_timestamp", "Results"), ("get_segment", "Results"),
        ("get_word", "Results"), ("get_token", "Results"), ("get_timings", "Results"),
        ("print_timings", "Results"), ("reset_timings", "Results"),
        ("tokenize", "Tokenize"), ("status_string", "Status"),
    ];

    private static readonly HashSet<string> s_csharpKeywords =
    [
        "abstract", "as", "base", "bool", "break", "byte", "case", "catch", "char",
        "checked", "class", "const", "continue", "decimal", "default", "delegate",
        "do", "double", "else", "enum", "event", "explicit", "extern", "false",
        "finally", "fixed", "float", "for", "foreach", "goto", "if", "implicit",
        "in", "int", "interface", "internal", "is", "lock", "long", "namespace",
        "new", "null", "object", "operator", "out", "override", "params", "private",
        "protected", "public", "readonly", "ref", "return", "sbyte", "sealed", "short",
        "sizeof", "stackalloc", "static", "string", "struct", "switch", "this",
        "throw", "true", "try", "typeof", "uint", "ulong", "unchecked", "unsafe",
        "ushort", "using", "virtual", "void", "volatile", "while",
        "value", "add", "remove", "get", "set",
    ];

    // ── Public API ─────────────────────────────────────────────────

    public string Generate(RustFfiParser parser)
    {
        var sb = new StringBuilder();
        WriteHeader(sb);
        WriteEnums(sb, parser);
        WriteHandles(sb);
        WriteCallbacks(sb);
        WriteStructs(sb, parser);
        WriteFunctions(sb, parser);
        return sb.ToString();
    }

    // ── Write sections ─────────────────────────────────────────────

    private void WriteHeader(StringBuilder sb)
    {
        sb.AppendLine("""
            // <auto-generated/>
            // Generated by TranscribeCppSharp.Generator from transcribe_sys.rs
            // Source: https://github.com/handy-computer/transcribe.cpp
            // DO NOT EDIT — regenerate with: dotnet run --project src/Generator

            #nullable enable

            using System;
            using System.Runtime.InteropServices;

            namespace TranscribeCppSharp.Interop;
            """);
    }

    private void WriteEnums(StringBuilder sb, RustFfiParser parser)
    {
        sb.AppendLine("// ════════════════════════════════════════════════════════════════");
        sb.AppendLine("// Enums");
        sb.AppendLine("// ════════════════════════════════════════════════════════════════");
        foreach (var e in parser.ParseEnums())
            WriteEnum(sb, e);
    }

    private void WriteHandles(StringBuilder sb)
    {
        sb.AppendLine("// ════════════════════════════════════════════════════════════════");
        sb.AppendLine("// Typed handles (opaque pointers with compile-time safety)");
        sb.AppendLine("// ════════════════════════════════════════════════════════════════");
        foreach (var csName in s_opaqueHandles.Values)
            WriteHandle(sb, csName);
    }

    private void WriteCallbacks(StringBuilder sb)
    {
        sb.AppendLine("""
            // ════════════════════════════════════════════════════════════════
            // Callback delegates
            // ════════════════════════════════════════════════════════════════
            [UnmanagedFunctionPointer(CallingConvention.Cdecl)]
            public delegate void LogCallback(LogLevel level, [MarshalAs(UnmanagedType.LPUTF8Str)] string msg, IntPtr userdata);

            [UnmanagedFunctionPointer(CallingConvention.Cdecl)]
            [return: MarshalAs(UnmanagedType.U1)]
            public delegate bool AbortCallback(IntPtr userdata);
            """);
    }

    private void WriteStructs(StringBuilder sb, RustFfiParser parser)
    {
        sb.AppendLine("// ════════════════════════════════════════════════════════════════");
        sb.AppendLine("// Structs");
        sb.AppendLine("// ════════════════════════════════════════════════════════════════");
        foreach (var s in parser.ParseStructs())
            WriteStruct(sb, s);
    }

    private void WriteFunctions(StringBuilder sb, RustFfiParser parser)
    {
        sb.AppendLine("// ════════════════════════════════════════════════════════════════");
        sb.AppendLine("// Native functions");
        sb.AppendLine("// ════════════════════════════════════════════════════════════════");
        sb.AppendLine("internal static partial class NativeMethods");
        sb.AppendLine("{");
        sb.AppendLine($"    private const string LibName = \"{_dllName}\";");
        sb.AppendLine();

        var functions = parser.ParseFunctions();
        var grouped = functions.GroupBy(f => ClassifyFunction(f.Name));
        foreach (var group in grouped.OrderBy(g => g.Key))
        {
            sb.AppendLine($"    // ── {group.Key} ──");
            foreach (var fn in group)
                WriteFunction(sb, fn);
            sb.AppendLine();
        }

        sb.AppendLine("}");
    }

    // ── Write individual elements ──────────────────────────────────

    private void WriteEnum(StringBuilder sb, RustEnum e)
    {
        var csName = ToPascalCase(e.TypeName);
        sb.AppendLine($"public enum {csName}");
        sb.AppendLine("{");
        foreach (var v in e.Values)
            sb.AppendLine($"    {ToPascalCase(v.Name)} = {v.Value},");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private void WriteHandle(StringBuilder sb, string csName)
    {
        sb.AppendLine($$"""
            [StructLayout(LayoutKind.Sequential)]
            public readonly struct {{csName}} : IEquatable<{{csName}}>
            {
                internal readonly IntPtr Value;
                internal {{csName}}(IntPtr value) => Value = value;

                public static implicit operator IntPtr({{csName}} h) => h.Value;
                public static explicit operator {{csName}}(IntPtr v) => new(v);
                public static {{csName}} Null => new(IntPtr.Zero);
                public bool IsNull => Value == IntPtr.Zero;

                public bool Equals({{csName}} other) => Value == other.Value;
                public override bool Equals(object? obj) => obj is {{csName}} h && Equals(h);
                public override int GetHashCode() => Value.GetHashCode();
                public override string ToString() => $"{{csName}}(0x{Value:X})";

                public static bool operator ==({{csName}} left, {{csName}} right) => left.Equals(right);
                public static bool operator !=({{csName}} left, {{csName}} right) => !left.Equals(right);
            }
            """);
        sb.AppendLine();
    }

    private void WriteStruct(StringBuilder sb, RustStruct s)
    {
        var csName = ToPascalCase(s.Name);
        sb.AppendLine("[StructLayout(LayoutKind.Sequential)]");
        sb.AppendLine($"public struct {csName}");
        sb.AppendLine("{");
        foreach (var f in s.Fields)
        {
            var csType = MapTypeForStructField(f.Type);
            var csField = ToCamelCase(f.Name);

            // Bool fields: MUST be 1 byte (Rust repr(C) bool = 1 byte)
            if (f.Type == "bool")
                sb.AppendLine("    [MarshalAs(UnmanagedType.I1)]");
            // String pointer fields in structs: borrowed pointers → IntPtr (safe)
            else if (f.Type.Contains("c_char"))
                csType = "IntPtr";

            sb.AppendLine($"    public {csType} {csField};");
        }
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private void WriteFunction(StringBuilder sb, RustFunction fn)
    {
        var csName = ToPascalCase(fn.Name);
        var parameters = ParseParams(fn.ParamsRaw);

        // Skip functions with callback pointer params (need manual delegate mapping)
        if (parameters.Any(p => p.Type.Contains("Option<unsafe")))
        {
            sb.AppendLine($"    // TODO: {fn.Name} — function pointer params need manual delegate mapping");
            sb.AppendLine();
            return;
        }

        // Detect return type: borrowed *const c_char → IntPtr (NOT string)
        var returnsBorrowedString = fn.ReturnType.Contains("c_char");
        var retType = returnsBorrowedString ? "IntPtr" : MapTypeForParam(fn.ReturnType);

        sb.AppendLine($"    [LibraryImport(LibName, EntryPoint = \"{fn.Name}\", StringMarshalling = StringMarshalling.Utf8)]");

        // Bool return: must be U1 (1 byte, not 4-byte Windows BOOL)
        if (retType == "bool")
            sb.AppendLine("    [return: MarshalAs(UnmanagedType.U1)]");

        var paramsStr = string.Join(", ", parameters.Select(p =>
        {
            var type = MapTypeForParam(p.Type);
            var name = ToCamelCase(p.Name);
            // String params: explicit UTF-8 marshalling
            if (p.Type.Contains("c_char"))
                return $"[MarshalAs(UnmanagedType.LPUTF8Str)] string {name}";
            // Bool params: must be I1 (1 byte)
            if (p.Type == "bool")
                return $"[MarshalAs(UnmanagedType.I1)] bool {name}";
            return $"{type} {name}";
        }));

        sb.AppendLine($"    public static partial {retType} {csName}({paramsStr});");
        sb.AppendLine();
    }

    // ── Type mapping (split by concern) ────────────────────────────

    /// <summary>
    /// Map a Rust type for function return/param context.
    /// Borrowed string pointers (*const c_char) return IntPtr — caller must marshal manually.
    /// </summary>
    private static string MapTypeForParam(string rustType)
    {
        rustType = rustType.Trim();
        return MapPrimitive(rustType)
            ?? MapOpaqueHandle(rustType)
            ?? MapStruct(rustType)
            ?? MapPointer(rustType)
            ?? throw new NotSupportedException($"Unknown Rust type: '{rustType}'");
    }

    /// <summary>
    /// Map a Rust type for struct field context.
    /// Bool gets MarshalAs I1, string pointers become IntPtr.
    /// </summary>
    private static string MapTypeForStructField(string rustType)
    {
        rustType = rustType.Trim();
        return rustType switch
        {
            "bool" => "bool",  // WriteStruct adds [MarshalAs(UnmanagedType.I1)]
            _ => MapTypeForParam(rustType),
        };
    }

    private static string? MapPrimitive(string rustType) => rustType switch
    {
        "()" or "" or null => "void",
        "void" => "void",
        "bool" => "bool",
        "i32" => "int",
        "u32" => "uint",
        "i64" => "long",
        "u64" => "ulong",
        "usize" => "nuint",
        "f32" => "float",
        "f64" => "double",
        "c_int" => "int",
        "c_uint" => "uint",
        "c_char" => "byte",
        // NOTE: *const c_char is NOT mapped here — it's a borrowed pointer.
        // Functions return IntPtr (manual marshal). Struct fields use IntPtr directly.
        _ => null,
    };

    private static string? MapOpaqueHandle(string rustType)
    {
        foreach (var (rustName, csHandle) in s_opaqueHandles)
        {
            if (rustType == $"*const {rustName}" || rustType == $"*mut {rustName}")
                return csHandle;
        }
        return null;
    }

    private static string? MapStruct(string rustType)
    {
        if (rustType.StartsWith("transcribe_") && !rustType.Contains('*'))
            return ToPascalCase(rustType);
        return null;
    }

    private static string? MapPointer(string rustType)
    {
        // All pointers → IntPtr (including *const c_char, *mut f32, etc.)
        if (rustType.StartsWith('*'))
            return "IntPtr";
        return null;
    }

    // ── Helpers ────────────────────────────────────────────────────

    private static string ClassifyFunction(string name)
    {
        foreach (var (pattern, category) in s_functionCategories)
        {
            if (name == pattern || name.StartsWith(pattern))
                return category;
        }
        return "Misc";
    }

    private static List<(string Type, string Name)> ParseParams(string raw)
    {
        if (string.IsNullOrWhiteSpace(raw) || raw == "()")
            return [];

        var result = new List<(string Type, string Name)>();
        var depth = 0;
        var current = new StringBuilder();
        foreach (var ch in raw)
        {
            if (ch is '(' or '<') depth++;
            else if (ch is ')' or '>') depth--;
            else if (ch == ',' && depth == 0)
            {
                ParseOneParam(current.ToString().Trim(), result);
                current.Clear();
                continue;
            }
            current.Append(ch);
        }
        if (current.Length > 0)
            ParseOneParam(current.ToString().Trim(), result);
        return result;
    }

    private static void ParseOneParam(string raw, List<(string Type, string Name)> result)
    {
        var colonIdx = raw.IndexOf(':');
        if (colonIdx < 0) return;
        result.Add((raw[(colonIdx + 1)..].Trim(), raw[..colonIdx].Trim()));
    }

    private static string ToPascalCase(string s)
    {
        s = Regex.Replace(s, @"^transcribe_", "", RegexOptions.IgnoreCase);
        return string.Join("", s.Split('_', StringSplitOptions.RemoveEmptyEntries)
            .Select(w => w.All(char.IsUpper)
                ? char.ToUpper(w[0]) + (w.Length > 1 ? w[1..].ToLower() : "")
                : char.ToUpper(w[0]) + (w.Length > 1 ? w[1..] : "")));
    }

    private static string ToCamelCase(string s)
    {
        var pascal = ToPascalCase(s);
        var camel = pascal.Length > 0 ? char.ToLower(pascal[0]) + pascal[1..] : pascal;
        return s_csharpKeywords.Contains(camel) ? "@" + camel : camel;
    }
}
